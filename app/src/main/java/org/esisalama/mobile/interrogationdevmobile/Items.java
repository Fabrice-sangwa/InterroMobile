package org.esisalama.mobile.interrogationdevmobile;

public class Items {
    private String login;
    private int id;

    public Items() {
        this.login = login;
    }

    public Items(String login, int id) {
        this.login = login;
        this.id = id;

    }

    public String getLogin() {
        return login;
    }

    public int getId() {
        return id;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public void setId(int id) {
        this.id = id;
    }

}
